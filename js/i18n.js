/* ── Notation converter FR → EN ── */
function lineToEN(lineStr) {
  return lineStr.replace(/\b([CFDT])(?=[a-hx1-8O])/g, function(m, p) {
    return {C:'N', F:'B', D:'Q', T:'R'}[p] || p;
  });
}
function getLine(lineStr) {
  return LANG === 'en' ? lineToEN(lineStr) : lineStr;
}

